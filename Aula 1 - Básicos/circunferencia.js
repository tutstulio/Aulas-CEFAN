// Cria uma constante
const PI = 3.14159265;

// Cria e inicia uma variável
var raio = 7;

// Calcula o perímetro da circunferência
var perimetro = 2 * PI * raio;

// Calcula a área da circunferência
var area = PI * raio ** 2;

// Mostra o resultado na tela
console.log("Perímetro = " + perimetro);
console.log("Área = " + area);